#include "libft.h"

char	*ft_strbchr(char *s, int c)
{
	int		i;

	i = 0;
	while(s[i])
	{
		if (c == s[i])
		{
			s[i + 1] = '\0';
			return (s);
		}
		i++;
	}
	if (c == '\0')
		return (s);
	return NULL;
}
